# 打印

# 单引号
print('hello world')

# 双引号
print("HELLO WORLD")

# 区别
print('hello world "Baby"')
print("HELLO WORLD 'baby'")

# 特殊字符处理
print('第一行\n第二行')   # 换行
print('1\tmale')      # 制表符
print('反斜杆\\')          # 反斜杆
print('单引号\'')         # 单引号
print("双引号\"")          # 双引号

# 用户输入
name = input('请输入用户名：')
print(name)