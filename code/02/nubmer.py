# 计算器
x = input('输入第一个整数：')   # 输入字符串1
y = input('输入第二个整数：')   # 输入字符串2
x_int = int(x)  # 转换成int类型
y_int = int(y)  # 转换成int类型
answer = x_int * y_int
print(x + ' * ' + y + ' = ' + str(answer))





